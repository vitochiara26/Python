numeros = [5, 3, 1, 2, 4]
vocales = ["o", "u", "a", "i", "e"]

print(f"Lista numeros: {numeros}")

numeros.sort()
print(f"\nLista numeros: {numeros}")

numeros.sort(reverse = True)
print(f"\nLista numeros: {numeros}")

print(f"\nLista vocales: {vocales}")

vocales.sort()
print(f"\nLista vocales: {vocales}")

vocales.sort(reverse = True)
print(f"\nLista vocales: {vocales}")